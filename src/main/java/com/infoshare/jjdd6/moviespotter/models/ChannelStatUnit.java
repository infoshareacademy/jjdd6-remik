package com.infoshare.jjdd6.moviespotter.models;

public class ChannelStatUnit {

    private String name;
    private String clickCounterPercentage;
    private int clickCounter;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClickCounterPercentage() {
        return clickCounterPercentage;
    }

    public void setClickCounterPercentage(String clickCounterPercentage) {
        this.clickCounterPercentage = clickCounterPercentage;
    }

    public int getClickCounter() {
        return clickCounter;
    }

    public void setClickCounter(int clickCounter) {
        this.clickCounter = clickCounter;
    }
}
